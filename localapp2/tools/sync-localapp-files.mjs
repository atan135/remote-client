import crypto from "node:crypto";
import fs from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "../..");
const defaultManifestPath = path.resolve(__dirname, "localapp-sync.manifest.json");
const managedHeaderPrefix = "// Synced from ";

const args = parseArgs(process.argv.slice(2));
const manifestPath = path.resolve(process.cwd(), args.manifest || defaultManifestPath);
const manifestDir = path.dirname(manifestPath);

const manifest = JSON.parse(await fs.readFile(manifestPath, "utf8"));
const sourceRoots = [
  path.resolve(repoRoot, "localapp/src"),
  path.resolve(repoRoot, "shared")
];
const targetRoots = [
  path.resolve(repoRoot, "localapp2/src/synced"),
  path.resolve(repoRoot, "localapp2/shared")
];

let hasDiff = false;

for (const entry of manifest.entries || []) {
  const sourcePath = path.resolve(manifestDir, entry.source);
  const targetPath = path.resolve(manifestDir, entry.target);

  assertWithinAllowedRoots(sourcePath, sourceRoots, "source");
  assertWithinAllowedRoots(targetPath, targetRoots, "target");

  const sourceText = await fs.readFile(sourcePath, "utf8");
  const generatedText = createManagedFileText(sourcePath, targetPath, sourceText);
  const currentText = await readTextIfExists(targetPath);

  if (currentText === generatedText) {
    console.log(`OK ${toRepoRelative(targetPath)}`);
    continue;
  }

  hasDiff = true;

  if (!args.write) {
    console.log(`DIFF ${toRepoRelative(targetPath)}`);
    continue;
  }

  await fs.mkdir(path.dirname(targetPath), { recursive: true });
  await fs.writeFile(targetPath, generatedText, "utf8");
  console.log(`SYNC ${toRepoRelative(sourcePath)} -> ${toRepoRelative(targetPath)}`);
}

if (hasDiff && !args.write) {
  process.exitCode = 1;
}

function parseArgs(values) {
  const parsed = {
    write: false,
    manifest: ""
  };

  for (let index = 0; index < values.length; index += 1) {
    const value = values[index];

    if (value === "--write") {
      parsed.write = true;
      continue;
    }

    if (value === "--check") {
      parsed.write = false;
      continue;
    }

    if (value === "--manifest") {
      parsed.manifest = values[index + 1] || "";
      index += 1;
      continue;
    }
  }

  return parsed;
}

function createManagedFileText(sourcePath, targetPath, sourceText) {
  const targetExtension = path.extname(targetPath);
  const sourceRelative = toRepoRelative(sourcePath);
  const sourceHash = sha256(sourceText);

  if (![".js", ".mjs", ".cjs"].includes(targetExtension)) {
    return sourceText;
  }

  const normalized = sourceText.replace(/^\s*\/\/ Synced from .*?\n(?:\/\/ .*?\n)*/u, "");

  return [
    `${managedHeaderPrefix}${sourceRelative}`,
    `// Source SHA-256: ${sourceHash}`,
    "// Do not edit this file directly.",
    "// Run: node localapp2/tools/sync-localapp-files.mjs --write",
    "",
    normalized
  ].join("\n");
}

async function readTextIfExists(filePath) {
  try {
    return await fs.readFile(filePath, "utf8");
  } catch (error) {
    if (error.code === "ENOENT") {
      return "";
    }

    throw error;
  }
}

function assertWithinAllowedRoots(candidatePath, roots, label) {
  const normalizedCandidate = path.resolve(candidatePath);
  const matched = roots.some((rootPath) => isWithin(normalizedCandidate, rootPath));

  if (!matched) {
    throw new Error(`Refusing to sync ${label} outside allowed roots: ${candidatePath}`);
  }
}

function isWithin(candidatePath, rootPath) {
  const relative = path.relative(rootPath, candidatePath);
  return relative === "" || (!relative.startsWith("..") && !path.isAbsolute(relative));
}

function toRepoRelative(filePath) {
  return path.relative(repoRoot, filePath).split(path.sep).join("/");
}

function sha256(value) {
  return crypto.createHash("sha256").update(value).digest("hex");
}
